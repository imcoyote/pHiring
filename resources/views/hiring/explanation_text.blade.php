    <h3>Vue d'ensemble</h3>
    <p class="text-justify">
        Cette page permet d'avoir un aperçu rapide des demandes d'engagement. Le tableau met en forme les principales données des demandes d'engagement. Il est aussi possible de faire des actions directement depuis cette vue. Pour avoir le détails d'une demande, il faut sélectionner "voir" de la demande que l'on veut voir.
    </p>
    <ul>
        <li>
            <p class="text-justify"><strong>Commenter : </strong>Afin de commenter une demande d'engagement, cliquez sur l'icône de commentaire et saisissez votre commentaire. Ensuite cliquer sur le bouton "commenter" et votre commentaire est ajouté à la demande d'engement. Cette fonctionnalité est disponible au travers de JavaScript, assurez-vous qu'il soit activé.</p>
        </li>
        <li>
            <p class="text-justify"><strong>Visualiser la demande initiale : </strong>Afin de visualiser la demande initiale, utiliser l'icône du PDF. La demande initiale s'affichera en format PDF.</p>
        </li>
        <li>
            <p class="text-justify"><strong>Suppression d'une demande : </strong>Il est possible de supprimer une demande à l'aide de la croix. Si vous supprimer une demande d'engagement, les acteurs du processus de validation de la demande seront avertis par email</p>
        </li>
        <li>
            <p class="text-justify"><strong>Voir les détails : </strong>Il est possible de voir les détails d'une demande en cliquant sur l'icône de la loupe.</p>
        </li>
    </ul>
